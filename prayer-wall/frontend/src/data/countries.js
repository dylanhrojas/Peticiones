// Get center coordinates for a country by its Spanish name.
// Returns { lat, lng } or null if not found.
export function coordsForName(name) {
  if (!name) return null
  const n = normalizeCountry(name.trim())
  const found = COUNTRIES.find(c => normalizeCountry(c.name) === n)
  if (!found || !COORDS[found.code]) return null
  const [lat, lng] = COORDS[found.code]
  return { lat, lng }
}

// Turn an ISO 3166-1 alpha-2 code into the matching flag emoji
// by shifting each letter into the regional indicator symbol range.
// e.g. "MX" → 🇲🇽
export function flagEmoji(code) {
  if (!code || code.length !== 2) return ''
  const OFFSET = 127397
  return String.fromCodePoint(...code.toUpperCase().split('').map(c => c.charCodeAt(0) + OFFSET))
}

// Look up the flag emoji for a country by its Spanish name.
// Returns empty string if not found.
export function flagForName(name) {
  if (!name) return ''
  const n = normalizeCountry(name.trim())
  const found = COUNTRIES.find(c => normalizeCountry(c.name) === n)
  return found ? flagEmoji(found.code) : ''
}

// Normalize a string for search: lowercase + remove diacritics
// ("México" / "mexico" / "MEXICO" all match).
export function normalizeCountry(s) {
  return s.normalize('NFD').replace(/[\u0300-\u036f]/g, '').toLowerCase()
}

// Filter + rank countries by a query string. Prefix matches rank higher
// than substring matches. Returns up to `limit` suggestions.
export function searchCountries(query, limit = 8) {
  const q = normalizeCountry(query.trim())
  if (!q) return []
  const matches = []
  for (const c of COUNTRIES) {
    const n = normalizeCountry(c.name)
    if (n.startsWith(q)) matches.push({ c, rank: 0 })
    else if (n.includes(q)) matches.push({ c, rank: 1 })
  }
  matches.sort((a, b) => {
    if (a.rank !== b.rank) return a.rank - b.rank
    return a.c.name.localeCompare(b.c.name, 'es')
  })
  return matches.slice(0, limit).map(m => m.c)
}
